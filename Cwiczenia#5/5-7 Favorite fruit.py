## 5-7. Favorite Fruit: Make a list of your favorite fruits, 
#   and then write a series of independent if statements that check
#      for certain fruits in your list.

# * Make a list of your three favorite fruits and call it favorite_fruits.

# * Write five if statements. Each should check whether a certain kind of 
#       fruit is in your list. If the fruit is in your list, the if block should
#           print a statement, such as You really like bananas!

favorite_fuits = ['strawberry', 'banana', 'kiwi']

if 'strawberry' in favorite_fuits:
    print("I love strawberries!")
if 'banana' in favorite_fuits:
    print("I love bananas!")
if 'kiwi' in favorite_fuits:
    print("I love kiwis!")
if  'raspberry' in favorite_fuits:
    print("I love raspberries!")
if 'tomato' in favorite_fuits:
    print("TOMATO IS A FRUIT!!!")

